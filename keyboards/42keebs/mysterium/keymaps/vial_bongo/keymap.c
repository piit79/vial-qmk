#define IDLE_SPEED 20
#define PREP_SPEED 40
uint8_t current_idle_frame = 0;
uint8_t current_tap_frame = 0;

 // assumes 1 frame prep stage
void animation_phase(void) {
  if (get_current_wpm() <= IDLE_SPEED) {
    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
    oled_write_raw_P(idle[abs((IDLE_FRAMES - 1) - current_idle_frame)],
                      ANIMATION_SIZE);
  }
  else if (get_current_wpm() <= PREP_SPEED) {
    oled_write_raw_P(prep[0], ANIMATION_SIZE);
  } else {
    current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
    oled_write_raw_P(tap[abs((TAP_FRAMES - 1) - current_tap_frame)],
                      ANIMATION_SIZE);
  }
}

#define ANIM_FRAME_DURATION 200
uint32_t anim_timer = 0;
uint32_t anim_sleep = 0;

static void render_animation(void) {
  if (get_current_wpm()) {
    oled_on(); 
    if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
      anim_timer = timer_read32();
      animation_phase();
    }
    anim_sleep = timer_read32();
  } else {
    if (OLED_TIMEOUT > 0 && timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
      oled_off();
    } else {
      if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) {
        anim_timer = timer_read32();
        animation_phase();
      }
    }
  }
}

char wpm_str[10];
bool oled_task_user(void) {
  // Animation
  render_animation(); 

  // WPM String
  oled_set_cursor(0, 0);  
  sprintf(wpm_str, "WPM:%03d", get_current_wpm());
  oled_write(wpm_str, false);

  // Host Keyboard LED Status
  led_t led_state = host_keyboard_led_state();
  oled_set_cursor(0, 1);
  oled_write_P(led_state.num_lock ? PSTR("NUM ") : PSTR("    "), false);
  oled_set_cursor(0, 2);
  oled_write_P(led_state.caps_lock ? PSTR("CAP ") : PSTR("    "), false);
  oled_set_cursor(0, 3);
  oled_write_P(led_state.scroll_lock ? PSTR("SCR ") : PSTR("    "), false);

  return false;
}
